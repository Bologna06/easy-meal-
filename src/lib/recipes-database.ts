// Função auxiliar para buscar receitas
export function getRecipesByCategory(category: Recipe['category']): Recipe[] {
  return recipesDatabase.filter(recipe => recipe.category === category);
}

export function getRecipesByTag(tag: string): Recipe[] {
  return recipesDatabase.filter(recipe => recipe.tags.includes(tag));
}

export function searchRecipes(query: string): Recipe[] {
  const lowerQuery = query.toLowerCase();
  return recipesDatabase.filter(recipe => 
    recipe.title.toLowerCase().includes(lowerQuery) ||
    recipe.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
    recipe.ingredients.some(ing => ing.toLowerCase().includes(lowerQuery))
  );
}

export function getRecipeById(id: number): Recipe | undefined {
  return recipesDatabase.find(recipe => recipe.id === id);
}

